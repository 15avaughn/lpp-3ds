#include <stdio.h>
#include <string.h>
#include <3ds.h>
#include <3ds/services/fs.h>
#include "include/luaplayer.h"
#include "include/luaGraphics.h"

const char *errMsg;
unsigned char *buffer;

int main()
{
	srvInit();	
	aptInit();
	gfxInit();
	hidInit(NULL);
	fsInit();
	Handle fileHandle;
	u64 size;
	u32 bytesRead;
	int restore;
			// This is only for testing purposes
		static lua_State *L;
		L = luaL_newstate();
		luaL_openlibs(L);
		luaScreen_init(L);
		luaL_loadstring(L,"while true do Screen.debugPrint(0,0,\"Hello World\",0xFFFFFF,1) end");
	while(aptMainLoop())
	{
		gspWaitForVBlank();
		hidScanInput();
		restore=0;
		

		
		
		// Load main script
		
		FS_path filePath=FS_makePath(PATH_CHAR, "/index.lua");
		FS_archive script=(FS_archive){ARCH_SDMC, (FS_path){PATH_EMPTY, 1, (u8*)""}};
		FSUSER_OpenFileDirectly(NULL, &fileHandle, script, filePath, FS_OPEN_READ, FS_ATTRIBUTE_NONE);
		FSFILE_GetSize(fileHandle, &size);
		buffer = (unsigned char*)(malloc((size+1) * sizeof (char)));
		FSFILE_Read(fileHandle, &bytesRead, 0x0, buffer, size);
		buffer[size]=0;
		FSFILE_Close(fileHandle);
		errMsg = runScript((const char*)buffer, true);
		free(buffer);
		
		if (errMsg != NULL);
                {
				char error[256];
				strcpy(error,"\nError: ");
				strcat(error,errMsg);
				strcat(error,"\n\nPress A to restart\nPress B to exit\n");
                DrawText(0,0,error, 0xFFFFFF, 1);
				}
						while (restore==0){
							if(hidKeysDown() & KEY_A){
								restore=1;
							}else if(hidKeysDown() & KEY_B){
								restore=2;
							}
						}
						if (restore==2){
						break;
						}
		gfxFlushBuffers();
		gfxSwapBuffers();
	}
	
	svcCloseHandle(fileHandle);
	fsExit();
	hidExit();
	gfxExit();
	aptExit();
	srvExit();

	return 0;
}
